export const getMoonPhaseIcon = (phase) => {
  switch (phase) {
    case "new":
      return "moon-outline";
    case "waxing-crescent":
      return "moon";
    case "full":
      return "sunny";
    case "waning-crescent":
      return "moon-outline";
    default:
      return "moon-outline";
  }
};

